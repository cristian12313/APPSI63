package pe.edu.upc.APPSI63.servicesimplements;

import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.stereotype.Service;
import pe.edu.upc.APPSI63.entities.Role;
import pe.edu.upc.APPSI63.repositories.RoleRepository;
import pe.edu.upc.APPSI63.servicesinterfaces.RoleService;

import java.util.List;
@Service
public class RoleServiceImplement implements RoleService {
    @Autowired
    private RoleRepository rR;

    @Override
    public void insert(Role rol) {
        rR.save(rol);
    }

    @Override
    public List<Role> list() {
        return rR.findAll();
    }

    @Override
    public void delete(Long idRol) {
        rR.deleteById(idRol);
    }

    @Override
    public Role listarId(Long idRol) {
        return rR.findById(idRol).orElse(new Role());
    }
}
